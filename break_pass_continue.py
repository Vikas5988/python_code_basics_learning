# Break Pass and Continue Statement

#Break Statement

for i in range(6):
    if (i==3):
        print("Break at  number 3")
        break
    print(i)
    
#Break Statement

for i in range(6):
    if (i==4):
        print("Skip at 4 number")
        continue
    print(i)

#Pass
for i in range(6):
      pass  # To Skip the error  and Do Nothing
        
